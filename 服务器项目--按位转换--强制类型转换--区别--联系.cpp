//
//        “把一个类型的‘数值’，转换为另一个类型能表示的‘数值’”
//
//        它关注的是：
//
//        数学意义
//
//        抽象值
//
//        语言层面的规则
//
//        ❗不关心内存中的 bit
//
//        2️⃣ cast 的行为特点
//
